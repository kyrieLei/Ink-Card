/* This callback is called by the HAL_UART_IRQHandler when the given number of bytes are received */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)
    {
        // USB Port

        /* Transmit one byte with 100 ms timeout */
        //forward to BLE
        //HAL_UART_Transmit(&huart2, &byte, 1, 100);

        //echo status to USB
//        HAL_UART_Transmit(huart, &byte, 1, 100);

        if (revc == 0) {
            char bufftr[] = "Receiving USB Image";
            HAL_UART_Transmit(&huart1, (uint8_t*)bufftr, sizeof(bufftr)-1, 100);
        }
        else if ((revc%100) == 0) {
            char bufftr[] = ".";
            HAL_UART_Transmit(&huart1, (uint8_t*)bufftr, sizeof(bufftr)-1, 100);
        }

        nfcBuffer[revc] = 255 - byte; //need invert
        revc++;
        if (revc >= 5000) {
            revc = 0;

            char bufftr[] = "\n\rRECEIVED 5000 BYTES!\n\r";
            HAL_UART_Transmit(&huart1, (uint8_t*)bufftr, sizeof(bufftr)-1, 100);

            EPD_1IN54_V2_Display((unsigned char *) nfcBuffer);
        }

        /* Receive one byte in interrupt mode */
        HAL_UART_Receive_IT(huart, &byte, 1);



    } else if (huart->Instance == USART2) {
        // BLE Port
        // forward to USB port
        HAL_UART_Transmit(&huart1, &byte, 1, 100);
    }
}

